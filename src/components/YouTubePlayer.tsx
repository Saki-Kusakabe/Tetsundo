'use client'

import { useEffect, useRef, useState, useCallback } from 'react'

interface YouTubePlayerProps {
  videoUrl: string
  onVideoComplete: (data: {
    videoUrl: string
    duration: number
    watchTime: number
    completionRate: number
  }) => void
}

interface YTPlayer {
  destroy(): void
  getDuration(): number
  getPlayerState(): number
  getCurrentTime(): number
}

interface YTEvent {
  target: YTPlayer
  data: number
}

declare global {
  interface Window {
    YT: {
      Player: new (element: HTMLElement, config: {
        height: string
        width: string
        videoId: string
        playerVars: Record<string, number | string> // 'width'„Å®'height'„Çístring„Å´„Åß„Åç„Çã„Çà„ÅÜ‰øÆÊ≠£
        events: {
          onReady: (event: YTEvent) => void
          onStateChange: (event: YTEvent) => void
          onError?: (event: { data: number }) => void
        }
      }) => YTPlayer
      PlayerState: {
        PLAYING: number
        PAUSED: number
        ENDED: number
      }
    }
    onYouTubeIframeAPIReady: () => void
  }
}

export default function YouTubePlayer({ videoUrl, onVideoComplete }: YouTubePlayerProps) {
  const playerRef = useRef<HTMLDivElement>(null)
  const [player, setPlayer] = useState<YTPlayer | null>(null)
  const [isApiReady, setIsApiReady] = useState(false)
  const [watchTime, setWatchTime] = useState(0)
  const [duration, setDuration] = useState(0)
  const [isPlaying, setIsPlaying] = useState(false)
  const [completionRate, setCompletionRate] = useState(0)
  const [error, setError] = useState<string | null>(null)
  const [isApiLoading, setIsApiLoading] = useState(false)
  const [hasCompleted, setHasCompleted] = useState(false)
  const intervalRef = useRef<NodeJS.Timeout | null>(null)

  // YouTubeÂãïÁîªID„ÇíÊäΩÂá∫
  const extractVideoId = (url: string): string | null => {
    // googleusercontent.com „Éâ„É°„Ç§„É≥„ÅÆÊ≠£Ë¶èË°®Áèæ„ÇíËøΩÂä†
    const regExp = /^.*(?:(?:youtube\.com\/(?:[^\/]+\/.+\/|(?:v|e(?:mbed)?)\/|.*[?&]v=))|youtu\.be\/|googleusercontent\.com\/(?:youtube\.com\/(?:1|2)\/))([^"&?\/\s]{11}).*/;
    const match = url.match(regExp);
    return match && match[1].length === 11 ? match[1] : null;
  }

  const videoId = extractVideoId(videoUrl)

  // „Çµ„Ç§„Ç∫Âº∑Âà∂„ÅÆÈñ¢Êï∞„Å®MutationObserver„ÇíÂâäÈô§„Åó„Åæ„Åô„ÄÇ
  // ÂøÖË¶Å„Å´Âøú„Åò„Å¶ `playerRef.current` „Å´Áõ¥Êé•„ÇØ„É©„Çπ„ÇíËøΩÂä†„Åô„ÇãÂΩ¢„Å´Â§âÊõ¥„Åó„Åæ„Åô„ÄÇ

  const startWatchTimeTracking = useCallback(() => {
    if (intervalRef.current) {
      clearInterval(intervalRef.current)
    }

    intervalRef.current = setInterval(() => {
      if (player && duration > 0) {
        try {
          const currentTime = player.getCurrentTime()
          const rate = currentTime / duration
          setWatchTime(Math.floor(currentTime))
          setCompletionRate(Math.min(rate, 1))

          if (rate >= 0.8 && !hasCompleted) {
            console.log('üéâ 80% completion achieved! Rate:', (rate * 100).toFixed(1) + '%')
            console.log('üìû Calling onVideoComplete callback...')
            setHasCompleted(true)
            onVideoComplete({
              videoUrl,
              duration,
              watchTime: Math.floor(currentTime),
              completionRate: rate
            })
          }

          if (Math.floor(currentTime) % 5 === 0) {
            console.log('üìä Progress:', (rate * 100).toFixed(1) + '%', `(${Math.floor(currentTime)}/${Math.floor(duration)}s)`)
          }
        } catch (error) {
          console.error('‚ùå Error getting current time:', error)
        }
      } else {
        console.log('‚ö†Ô∏è Player not ready:', { player: !!player, duration })
      }
    }, 1000)
  }, [player, duration, hasCompleted, onVideoComplete, videoUrl])

  const stopWatchTimeTracking = useCallback(() => {
    if (intervalRef.current) {
      clearInterval(intervalRef.current)
      intervalRef.current = null
    }
  }, [])

  const handleVideoEnd = useCallback(() => {
    if (!player || duration === 0) return

    try {
      const currentTime = player.getCurrentTime()
      const finalCompletionRate = Math.min(currentTime / duration, 1)

      console.log('Video completed:', {
        duration,
        currentTime: Math.floor(currentTime),
        completionRate: finalCompletionRate
      })

      if (finalCompletionRate >= 0.8) {
        onVideoComplete({
          videoUrl,
          duration,
          watchTime: Math.floor(currentTime),
          completionRate: finalCompletionRate
        })
      }
    } catch (error) {
      console.error('Error in handleVideoEnd:', error)
    }
  }, [player, duration, onVideoComplete, videoUrl])

  useEffect(() => {
    // YouTube IFrame API „ÇíÂãïÁöÑ„Å´Ë™≠„ÅøËæº„Åø
    if (!window.YT) {
      setIsApiLoading(true)
      setError(null)

      const tag = document.createElement('script')
      // „Çπ„ÇØ„É™„Éó„Éà„ÅÆURL„Çí‰øÆÊ≠£ (googleusercontent.com/youtube.com/2 „ÅØÂÖ¨ÂºèAPI„ÅÆURL„Åß„ÅØ„Å™„ÅÑ„Çà„ÅÜ„Åß„Åô)
      tag.src = 'https://www.youtube.com/iframe_api'
      tag.onload = () => {
        console.log('YouTube API script loaded')
      }
      tag.onerror = () => {
        setError('YouTube API„ÅÆË™≠„ÅøËæº„Åø„Å´Â§±Êïó„Åó„Åæ„Åó„Åü')
        setIsApiLoading(false)
      }

      const firstScriptTag = document.getElementsByTagName('script')[0]
      firstScriptTag.parentNode?.insertBefore(tag, firstScriptTag)

      window.onYouTubeIframeAPIReady = () => {
        setIsApiReady(true)
        setIsApiLoading(false)
        console.log('YouTube API ready')
      }

      const timeout = setTimeout(() => {
        if (!isApiReady) {
          setError('YouTube API„ÅÆË™≠„ÅøËæº„Åø„Åå„Çø„Ç§„É†„Ç¢„Ç¶„Éà„Åó„Åæ„Åó„Åü')
          setIsApiLoading(false)
        }
      }, 10000)

      return () => clearTimeout(timeout)
    } else {
      setIsApiReady(true)
    }
  }, [isApiReady]) // isApiReady „Çí‰æùÂ≠òÈÖçÂàó„Å´ËøΩÂä†„Åó„ÄÅAPI„ÅåÊ∫ñÂÇô„Åß„Åç„ÅüÂæå„Å´ÂÜçÂ∫¶ÂÆüË°å„Åï„Çå„Å™„ÅÑ„Çà„ÅÜ„Å´„Åô„Çã

  useEffect(() => {
    if (!isApiReady || !videoId || !playerRef.current) return

    if (player) {
      player.destroy()
      setPlayer(null)
    }

    setWatchTime(0)
    setCompletionRate(0)
    setIsPlaying(false)
    setDuration(0)
    setHasCompleted(false)

    const newPlayer = new window.YT.Player(playerRef.current, {
      // height „Å® width „ÅØ„ÄÅCSS„ÅßÂà∂Âæ°„Åô„Çã„Åü„ÇÅ„ÄÅ„Åì„Åì„Åß„ÅØ'100%'„Å®'100%'„ÇíË®≠ÂÆö„Åó„Åæ„Åô„ÄÇ
      height: '100%',
      width: '100%',
      videoId: videoId,
      playerVars: {
        playsinline: 1,
        rel: 0,
        modestbranding: 1,
        controls: 1,
        showinfo: 0,
        fs: 0, // „Éï„É´„Çπ„ÇØ„É™„Éº„É≥„Éú„Çø„É≥„ÇíÈùûË°®Á§∫
        disablekb: 1,
        iv_load_policy: 3,
        cc_load_policy: 0,
        enablejsapi: 1,
        autoplay: 0,
        start: 0,
        html5: 1
      },
      events: {
        onReady: (event: YTEvent) => {
          try {
            const videoDuration = event.target.getDuration()
            setDuration(videoDuration)
            setError(null)
            console.log('üé¨ YouTube Player ready!')
            console.log('üìä Video duration:', videoDuration, 'seconds')
            console.log('üîó Video URL:', videoUrl)
          } catch (err) {
            console.error('Error in onReady:', err)
            setError('„Éó„É¨„Ç§„É§„Éº„ÅÆÂàùÊúüÂåñ„Å´Â§±Êïó„Åó„Åæ„Åó„Åü')
          }
        },
        onError: (event: { data: number }) => {
          console.error('YouTube player error:', event.data)
          switch (event.data) {
            case 2:
              setError('ÂãïÁîªID„ÅåÁÑ°Âäπ„Åß„Åô')
              break
            case 5:
              setError('HTML5„Éó„É¨„Ç§„É§„Éº„Åß„Ç®„É©„Éº„ÅåÁô∫Áîü„Åó„Åæ„Åó„Åü')
              break
            case 100:
              setError('ÂãïÁîª„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì')
              break
            case 101:
            case 150:
              setError('„Åì„ÅÆÂãïÁîª„ÅØÂüã„ÇÅËæº„ÅøÂÜçÁîü„ÅåË®±ÂèØ„Åï„Çå„Å¶„ÅÑ„Åæ„Åõ„Çì')
              break
            default:
              setError('ÂãïÁîª„ÅÆÂÜçÁîü‰∏≠„Å´„Ç®„É©„Éº„ÅåÁô∫Áîü„Åó„Åæ„Åó„Åü')
          }
        },
        onStateChange: (event: YTEvent) => {
          const playerState = event.data
          console.log('üéµ Player state changed:', playerState)

          if (playerState === window.YT.PlayerState.PLAYING) {
            console.log('‚ñ∂Ô∏è Video is now playing')
            setIsPlaying(true)
          } else if (
            playerState === window.YT.PlayerState.PAUSED ||
            playerState === window.YT.PlayerState.ENDED
          ) {
            console.log('‚è∏Ô∏è Video paused or ended')
            setIsPlaying(false)

            if (playerState === window.YT.PlayerState.ENDED) {
              console.log('üèÅ Video ended, calling handleVideoEnd')
              handleVideoEnd()
            }
          }
        }
      }
    })

    setPlayer(newPlayer)

    // „ÇØ„É™„Éº„É≥„Ç¢„ÉÉ„Éó
    return () => {
      newPlayer?.destroy()
    }
  }, [isApiReady, videoId]) // enforcePlayerSize „ÅÆ‰æùÂ≠ò„ÇíÂâäÈô§

  useEffect(() => {
    if (isPlaying && player && duration > 0) {
      startWatchTimeTracking()
    } else {
      stopWatchTimeTracking()
    }

    return () => {
      stopWatchTimeTracking()
    }
  }, [isPlaying, player, duration, startWatchTimeTracking, stopWatchTimeTracking])

  const formatTime = (seconds: number): string => {
    const mins = Math.floor(seconds / 60)
    const secs = seconds % 60
    return `${mins}:${secs.toString().padStart(2, '0')}`
  }

  if (!videoId) {
    return (
      <div className="w-full h-64 bg-gray-200 flex items-center justify-center rounded-lg">
        <p className="text-gray-600">ÁÑ°Âäπ„Å™YouTube URL„Åß„Åô</p>
      </div>
    )
  }

  if (error) {
    return (
      <div className="w-full max-w-4xl mx-auto">
        <div className="w-full h-64 bg-red-50 border border-red-200 flex items-center justify-center rounded-lg">
          <div className="text-center text-red-700">
            <div className="text-4xl mb-2">‚ö†Ô∏è</div>
            <p className="font-medium">{error}</p>
            <button
              onClick={() => {
                setError(null)
                setIsApiReady(false)
                setIsApiLoading(false)
                window.location.reload()
              }}
              className="mt-2 px-4 py-2 bg-red-600 text-white rounded hover:bg-red-700"
            >
              ÂÜçË™≠„ÅøËæº„Åø
            </button>
          </div>
        </div>
      </div>
    )
  }

  return (
    <div className="w-full max-w-4xl mx-auto">
      {/* ÂãïÁîª„Éó„É¨„Ç§„É§„ÉºÂÆπÂô® - CSS„ÇØ„É©„Çπ„Åß„Ç¢„Çπ„Éö„ÇØ„ÉàÊØî„ÇíÁ∂≠ÊåÅ */}
      <div className="youtube-player-wrapper">
        {isApiReady ? (
          <div
            ref={playerRef}
            // „Åì„Åì„Åß„ÅØ `absolute` „ÇØ„É©„Çπ„ÅÆ„Åø„ÇíÈÅ©Áî®„Åó„ÄÅÊÆã„Çä„ÅÆ„Çπ„Çø„Ç§„É´„ÅØCSS„Éï„Ç°„Ç§„É´„ÅßÂÆöÁæ©„Åó„Åæ„Åô„ÄÇ
            className="absolute top-0 left-0 w-full h-full"
            style={{ pointerEvents: 'auto' }} // ÂøÖË¶Å„Åß„ÅÇ„Çå„Å∞
          />
        ) : (
          <div className="absolute top-0 left-0 w-full h-full flex items-center justify-center">
            <div className="text-center text-white">
              <div className="text-4xl mb-2">
                {isApiLoading ? '‚è≥' : 'üé¨'}
              </div>
              <p>
                {isApiLoading
                  ? 'YouTube „Éó„É¨„Ç§„É§„Éº„ÇíË™≠„ÅøËæº„Åø‰∏≠...'
                  : 'YouTube „Éó„É¨„Ç§„É§„Éº„ÇíÊ∫ñÂÇô‰∏≠...'
                }
              </p>
            </div>
          </div>
        )}
      </div>

      {/* ÈÄ≤ÊçóÊÉÖÂ†± */}
      <div className="mt-4 p-4 bg-white rounded-lg shadow-sm">
        <div className="flex justify-between items-center mb-2">
          <div className="flex items-center space-x-4">
            <span className="text-sm text-gray-600">
              Ë¶ñËÅ¥ÊôÇÈñì: {formatTime(watchTime)}
            </span>
            {duration > 0 && (
              <span className="text-sm text-gray-600">
                ÂãïÁîªÊôÇÈñì: {formatTime(duration)}
              </span>
            )}
            <span className={`text-sm font-medium ${isPlaying ? 'text-green-600' : 'text-gray-600'}`}>
              {isPlaying ? 'ÂÜçÁîü‰∏≠' : 'ÂÅúÊ≠¢‰∏≠'}
            </span>
          </div>
          <span className="text-sm font-medium text-blue-600">
            {(completionRate * 100).toFixed(1)}% ÂÆå‰∫Ü
          </span>
        </div>

        {/* „Éó„É≠„Ç∞„É¨„Çπ„Éê„Éº */}
        <div className="w-full bg-gray-200 rounded-full h-3 mb-3">
          <div
            className={`h-3 rounded-full transition-all duration-300 ${completionRate >= 0.8 ? 'bg-green-500' : 'bg-blue-500'
              }`}
            style={{ width: `${Math.min(completionRate * 100, 100)}%` }}
          ></div>
        </div>

        <div className="flex justify-between text-sm">
          <p className="text-gray-600">
            ‚Äª 80%‰ª•‰∏äË¶ñËÅ¥„ÅßÈßÖ„Çí1„Å§ÈÄ≤„Åø„Åæ„Åô
          </p>
          {completionRate >= 0.8 && (
            <p className="text-green-600 font-medium">
              ‚úì ÈÅîÊàêÊù°‰ª∂„ÇØ„É™„Ç¢ÔºÅ
            </p>
          )}
        </div>
      </div>
    </div>
  )
}